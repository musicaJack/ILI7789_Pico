// 绘制单个字符
void ili9488_draw_char(uint16_t x, uint16_t y, unsigned char c, uint16_t color, uint16_t bg, uint8_t size) {
    if ((x >= 320) || // 超出屏幕宽度
        (y >= 480) || // 超出屏幕高度
        ((x + 5 * size - 1) < 0) || // 超出左边界
        ((y + 7 * size - 1) < 0)) { // 超出上边界
        return;
    }
    
    // ASCII字符范围检查
    if (c < 32 || c > 127) {
        c = '?'; // 对于不支持的字符显示问号
    }
    
    // 字符索引
    c -= 32;
    
    // 设置绘图窗口
    for (uint8_t i = 0; i < 5; i++) {
        uint8_t line = font5x7[c * 5 + i];
        for (uint8_t j = 0; j < 7; j++) {
            if (line & 0x01) {
                if (size == 1) {
                    ili9488_draw_pixel(x + i, y + j, color);
                } else {
                    ili9488_fill_rect(x + i * size, y + j * size, size, size, color);
                }
            } else if (bg != color) {
                // 如果背景色与前景色不同，则绘制背景色
                if (size == 1) {
                    ili9488_draw_pixel(x + i, y + j, bg);
                } else {
                    ili9488_fill_rect(x + i * size, y + j * size, size, size, bg);
                }
            }
            line >>= 1;
        }
    }
}

// 绘制字符串
void ili9488_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg, uint8_t size) {
    uint16_t cursor_x = x;
    uint16_t cursor_y = y;
    
    // 逐个绘制字符
    while (*str) {
        // 检查换行符
        if (*str == '\n') {
            cursor_x = x;
            cursor_y += size * 8;
        }
        // 检查回车符
        else if (*str == '\r') {
            cursor_x = x;
        }
        // 普通字符
        else {
            ili9488_draw_char(cursor_x, cursor_y, *str, color, bg, size);
            cursor_x += size * 6; // 字符宽度为5，加上1个像素间距
            
            // 行自动换行
            if (cursor_x > (320 - size * 5)) {
                cursor_x = x;
                cursor_y += size * 8;
            }
        }
        str++;
    }
}
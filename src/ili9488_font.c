// 将RGB565颜色转换为RGB666格式（18位）
static void rgb565_to_rgb666(uint16_t color, uint8_t *r, uint8_t *g, uint8_t *b) {
    // 从RGB565中提取分量
    *r = (color >> 11) & 0x1F;  // 5位红色
    *g = (color >> 5) & 0x3F;   // 6位绿色
    *b = color & 0x1F;          // 5位蓝色
    
    // 扩展到RGB666格式（每个颜色分量占用6位）
    *r = (*r << 1) | (*r >> 4); // 将5位红色扩展到6位
    // 绿色已经是6位，不需要扩展
    *b = (*b << 1) | (*b >> 4); // 将5位蓝色扩展到6位
}

